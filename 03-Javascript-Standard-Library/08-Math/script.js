// Math
// Math hanya bisa digunakan untuk tipe data Number

console.log(Math.min(1, 2, 3, 4, 5, 6, 7, 8)); // 1
console.log(Math.max(1, 2, 3, 4, 5, 6, 7, 8)); // 8
console.log(Math.round(5.5)); // 6 // lebih dekat ke 6
console.log(Math.round(5.6)); // 6
console.log(Math.round(5.4)); // 5 // lebih dekat ke 5
console.log(Math.floor(5.4)); // 5 // pembulatan selalu ke bawah
console.log(Math.floor(5.5)); // 5
console.log(Math.floor(5.6)); // 5
